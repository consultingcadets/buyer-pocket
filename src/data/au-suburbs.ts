export function searchSuburbs(query: string, limit = 20): AustralianSuburb[] {
  if (!query || query.length < 1) return [];
  const q = query.toLowerCase().trim();
  const starts: AustralianSuburb[] = [];
  const contains: AustralianSuburb[] = [];
  for (const s of AU_SUBURBS) {
    const name = s.suburb.toLowerCase();
    if (name.startsWith(q)) {
      starts.push(s);
    } else if (name.includes(q)) {
      contains.push(s);
    }
    if (starts.length + contains.length >= limit * 2) break;
  }
  return [...starts, ...contains].slice(0, limit);
}
